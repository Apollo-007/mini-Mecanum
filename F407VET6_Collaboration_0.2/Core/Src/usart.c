//串口的DMA发送
static void UART_TX_DMA_Send(uint8_t *buffer,uint16_t length)
{
  //等待上一次的数据发送完毕
  while(HAL_DMA_GetState(&hdma_usart1_tx) == HAL_DMA_STATE_BUSY);
  //关闭DMA
  __HAL_DMA_DISABLE(&hdma_usart1_tx);
   //开始发送数据
  HAL_UART_Transmit_DMA(&huart1,buffer,length);
}

//串口的调试输出语句
void Debug_printf(const char *format, ...)
{
  uint32_t length = 0;
	va_list args;
	__va_start(args,format);
	length = vsnprintf((char*)Usart_tx_buffer,sizeof(Usart_tx_buffer),(char*)format, args);
  __va_end(args);
	UART_TX_DMA_Send(Usart_tx_buffer, length);
  //延时5ms左右，避免串口输出乱码
  delay_ms(5);
}

